        {
            int key = (int)Math.Floor(time * 10);
            double mass;
            mass = TryRead(key, 1);
            if (mass > -1) { return mass; }
            mass = TryRead(key + 1, 1);
            if (mass > -1) { return mass; }
            mass = TryRead(key + 2, 1);
            if (mass > -1) { return mass; }
            mass = TryRead(key - 1, 1);
            if (mass > -1) { return mass; }
            mass = TryRead(key + 3, 1);
            if (mass > -1) { return mass; }
            Console.WriteLine("bad result of mass at T_{0}", time);
            return 10000;
        }

        public static double GetThrust(double time)
        {
            int key = (int)Math.Floor(time * 10);
